package movie.domain;

public class MovieMember {
	private int moviemember_idx;
	private String id;
	private String pass;
	private String name;
	public int getMoviemember_idx() {
		return moviemember_idx;
	}
	public void setMoviemember_idx(int moviemember_idx) {
		this.moviemember_idx = moviemember_idx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
